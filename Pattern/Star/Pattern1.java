public class Pattern1 {
    
        public static void main(String[] args) {
            int cnt1=5;
                for(int i=1;i<=5;i++)
                {
                    int cnt2=cnt1;
                    for(int j=1;j<=5;j++)
                    {
      System.out.print(cnt2);
      cnt2--;
                    }
                    
                    System.out.println();
                    cnt1--;
                }
    }
    }
    
    
    
    


package Method;

public class AdditionDemo {
     int a;
     int b;
     int result;

    public int addition()
    {
        result = a+b;
        return result;
    }

    public int substraction()
    {
        result = a-b;
        return result;
    }

    public int multiplication()
    {
        result = a*b;
        return result;
    }

    public int division()
    {
        result = a/b;
        return result;
    }
}
